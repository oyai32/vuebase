<template>
  <div class="login-wrap">
    <div class="ms-login">
      <div class="ms-title">后台管理系统</div>
      <el-form :model="ruleForm" :rules="rules" ref="ruleForm" label-width="0px" class="ms-content">
        <el-form-item prop="username">
          <el-input v-model="ruleForm.username" placeholder="用户名">
          </el-input>
        </el-form-item>
        <el-form-item prop="password">
          <el-input type="password" placeholder="密码" v-model="ruleForm.password"
                    @keyup.enter.native="submitForm('ruleForm')">
          </el-input>
        </el-form-item>
        <div class="login-btn">
          <el-button type="primary" @click="submitForm('ruleForm')">登录</el-button>
        </div>
        <p class="login-tips">Tips :<br/> 用户名admin，密码随便填（浅灰）<br/> 用户名oyyl，密码随便填（深黑）</p>
      </el-form>
    </div>
  </div>
</template>

<script>

  export default {
    data: function () {
      return {
        ruleForm: {
          username: '',
          password: ''
        },
        rules: {
          username: [
            {required: true, message: '请输入用户名', trigger: 'blur'}
          ],
          password: [
            {required: true, message: '请输入密码', trigger: 'blur'}
          ]
        }
      }
    },
    created() {
      // 直接访问的登陆页要执行logout相关操作
      this.$store.dispatch('user/Logout')
    },
    methods: {
      submitForm(formName) {
        this.$refs[formName].validate(async (valid) => {
          if (valid) {
            try {
              const res = await this.$store.dispatch('user/GetUserInfo', this.ruleForm)
              if (res.state === 'success') {
                this.$message.closeAll();
                this.$router.push('/');
              } else {
                this.Utils.showMsg(res.message, 'error')
              }
            } catch (e) {
              this.Utils.showMsg('登录失败！', 'error')
            }
          } else {
            return false;
          }
        });
      }
    }
  }
</script>

